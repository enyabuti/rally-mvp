-- ─── HELPER FUNCTION: CHECK MAJORITY ────────
-- Returns the option_id with majority votes, or null
create or replace function check_majority(p_trip_id uuid)
returns uuid as $$
declare
  total_members integer;
  winning_option uuid;
begin
  -- Count committed members
  select count(*) into total_members
  from members
  where trip_id = p_trip_id and status = 'committed';

  -- Find option with > 50% votes
  select option_id into winning_option
  from votes
  where trip_id = p_trip_id
  group by option_id
  having count(*) > total_members / 2.0
  order by count(*) desc
  limit 1;

  return winning_option;
end;
$$ language plpgsql;
